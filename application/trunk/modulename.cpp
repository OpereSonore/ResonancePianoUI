

#include "modulename.h"

/******************************************************************************/
ModuleName::ModuleName(void)
/******************************************************************************/
{
}

/******************************************************************************/
ModuleName::ModuleName (const char *file)
/******************************************************************************/
{
	QFileInfo file_info(file);

	_Module_Name = file_info.fileName().section(".", 0, 0);
}

/******************************************************************************/
ModuleName::~ModuleName (void)
/******************************************************************************/
{
}

/******************************************************************************/
QString &ModuleName::Get(void)
/******************************************************************************/
{
	return (_Module_Name);
}

